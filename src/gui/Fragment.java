    public int stringToInt(String str){
        double value = Double.parseDouble(str);
        
        double dif = max - min;
        double v = value - min;
        if(dif == 0)
           return 0; 
        return ((int)(100 * (v/dif)));
    }
    
    public String intToString(int value){
        double dif = max - min;
        double v = (value/100.0)*dif + min;
        
        return ("" + v);
    }
