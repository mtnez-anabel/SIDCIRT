    double min, max;
    Converter<Integer, String> converter = new Converter<Integer, String>() {

        @Override
        public String convertForward(Integer value) {
            double dif = max - min;
            double v = (value/100.0)*dif + min;

            return ("" + v);
        }

        @Override
        public Integer convertReverse(String str) {
            double value = Double.parseDouble(str);

            double dif = max - min;
            double v = value - min;
            if(dif == 0)
               return 0; 
            return ((int)(100 * (v/dif)));
        }
    };
    
